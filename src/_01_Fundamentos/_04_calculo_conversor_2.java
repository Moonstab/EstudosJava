package _01_Fundamentos;

public class _04_calculo_conversor_2 {

    public static void main(String[] args) {
        //Conversor real para dolar
        double real = 1;
        double dol = 5.53;
        double conv = real * dol;
        System.out.println("O valor do real em dolar na data 09/10/2020 é " + conv);

        //Conversor real para dolar
        real = 1;
        dol = 5.07;
        conv = real * dol;
        System.out.println("O valor do real em dolar na data 11/12/2021 é " + conv);

        //Conversor real para dolar
        real = 1;
        dol = 5.49;
        conv = real * dol;
        System.out.println("O valor do real em dolar na data 11/01/2021 é " + conv);
    }
}
